# Escape Sequence Character
# Escape sequence characters are used to represent special characters in a string.
# They are denoted by a backslash (\) followed by a specific character. 


# Here are some common escape sequence characters:
# 1. \n : Represents a new line.
# 2. \t : Represents a tab.
# 3. \\ : Represents a backslash.
# 4. \' : Represents a single quote.
# 5. \" : Represents a double quote.
# 6. \; : Represents a semicolon.
# There are many more escape sequence characters, but these are some of the most commonly used ones.

# Example of using escape sequence characters:
string = "Hello \n World!"
print(string) # World will be printed on a new line

string2 = "Hello \t Everyone!"
print(string2) # Output: Hello     Everyone! (Hello and Everyone will be separated by a tab space)

string3 = "This is a backslash: \\"
print(string3) # Output: This is a backslash: \

string4 = "She said, \'Hello!\'"
print(string4) # Output: She said, 'Hello!'