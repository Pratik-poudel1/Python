# Variable and Data Types
# Variable is a container for storing data values. 
# It can hold different types of data, such as numbers, strings, lists, etc.
# In Python, you don't need to declare the type of a variable.
# You can simply assign a value to a variable, and Python will automatically determine its type.

# Example of variable assignment
x = 10          # x is an integer
y = 3.14        # y is a float
name = "Alice"   # name is a string
# You can also change the value of a variable, and it can hold a different type of data.
x = "Hello"     # Now x is a string

# Python has several built-in data types that you can use to store different kinds of data.
# Data types in Python include:
# 1. Numeric Types: int, float
# 2. Text Type: str
# 3. Boolean Type: bool
# 4. None Type: NoneType

# Example of different data types
a = 5           # int
b = 2.5         # float
name = "Bob"    # str
is_valid = True  # bool
c = None          # NoneType


# Rules for Variable Names
# 1. Variable names must start with a letter (a-z, A-Z) or an underscore (_).
# 2. Variable names can contain letters, digits (0-9), and underscores.
# 3. Variable names are case-sensitive (e.g., myVar and myvar are different variables).
# 4. Variable names cannot be the same as Python keywords (e.g., if, else, while, etc.).

# Example of valid variable names
my_variable = 10
_variable = 20
myVar123 = 30

# Example of invalid variable names
# 1variable = 10  # Invalid: starts with a digit
# my-variable = 20  # Invalid: contains a hyphen
