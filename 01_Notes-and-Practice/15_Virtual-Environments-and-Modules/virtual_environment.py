# Virtual Environment:
# - An isolated environment for a project.
# - A virtual environment is a directory that contains all the libraries required for a project.

# Create a virtual environment:
# - Create a directory for the virtual environment.
# - Install the required libraries in the virtual environment.
# - Activate the virtual environment.
# - Run the project in the virtual environment.

# To create a virtual environment, we need to use the command:
# - python -m venv <name_of_virtual_environment>

# To activate a virtual environment, we need to use the command:
# - source <name_of_virtual_environment>/bin/activate

# To deactivate a virtual environment, we need to use the command:
# - deactivate

# To install a library in a virtual environment, we need to use the command:
# - pip install <library_name>

# To remove a library from a virtual environment, we need to use the command:
# - pip uninstall <library_name>

# To remove a virtual environment, we need to use the command:
# - rm -rf <name_of_virtual_environment>

# To remove all virtual environments, we need to use the command:
# - rm -rf .venv

#================================================================

# To install virtualenv, we need to use the command:
# pip install virtualenv

# To upgrade virtualenv, we need to use the command:
# pip install --upgrade virtualenv

# To create a virtual environment,
# virtualenv environment_name

# To activate a virtual environment,
# source environment_name/bin/activate

# To deactivate a virtual environment,
# deactivate

# We can install different version of module in different virtual environment.
